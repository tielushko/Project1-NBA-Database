class Coach:
    def __init__(self, coach_id, first_name, last_name, season_win, season_loss, playoff_win, playoff_loss, team):
        self.coach_id = coach_id
        self.first_name = first_name
        self.last_name = last_name
        self.season_win = season_win
        self.season_loss = season_loss
        self.playoff_win = playoff_win
        self.playoff_loss = playoff_loss
        self.team = team




